let n = 1;
while( n < 15 ){
    //bloque de código a ejecutar
    document.write( n , '<br>');
    n++;
}

document.write('<hr>');
// -----

do{
    document.write('iteración');
    n++;
}while( n < 15 );

document.write('<hr>');
